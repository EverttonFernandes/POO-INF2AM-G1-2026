package Dia0203PrimeirosPassosComJava.Exemplo2TiposDeDados;

public class Main {
    public static void main(String[] args) {
        // Principais tipos de dados primitivos em Java
        int numeroInteiro = 42; // Tipo inteiro
        double numeroDecimal = 3.14; // Tipo decimal
        Float valorMonetario = 100.999f; // Tipo float, usado para valores monetários
        char letra = 'A'; // Tipo caractere
        boolean valorVerdadeiro = true; // Tipo booleano pra dizer se é verdadeiro ou falso
        String mensagem = "Olá mundo!"; // Tipo String, usado para texto
    }
}
